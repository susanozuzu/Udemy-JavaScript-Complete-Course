

//What is Conditional-Ternary Operator is the only Javascript operator that takes three operands: a condition followed by a question mark(?), then an expression to execute if the condition is truthy followed by a colon (:), and finally an expression to execute if the condition is false

//Conditional-Ternary Operator is the only Javascript operator that allows us to write statements in one lines

//Example of Conditional-Ternary Operator
const age = 23;
age >= 18 ? console.log('I like to drink wine') : console.log('I like to drink water');

//Another example of Conditional

const drink = age >= 18 ? 'wine' : 'water';
console.log(drink);